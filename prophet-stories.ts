// Helper function to get prophets by age group
export const getProphetsByAgeGroup = (ageGroup: 'children' | 'teens' | 'adults'): ProphetStoryComplete[] => {
  return PROPHET_STORIES.filter(p => p.ageGroups.includes(ageGroup));
};

// Helper function to search prophets
export const searchProphets = (query: string): ProphetStoryComplete[] => {
  const lowercaseQuery = query.toLowerCase();
  return PROPHET_STORIES.filter(p =>
    p.nameArabic.includes(query) ||
    p.name.toLowerCase().includes(lowercaseQuery) ||
    p.title.toLowerCase().includes(lowercaseQuery) ||
    p.fullStory.toLowerCase().includes(lowercaseQuery)
  );
};
